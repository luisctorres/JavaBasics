package com.careerdevs.objectfactory;

public class CarFactory {

    public String make;
    public String model;
    public int mileage;
    public float gasTankPercent;

    //Constructor
    public CarFactory(String make, String model, int mileage, float gasTankPercent) {
        this.make = make;
        this.model = model;
        this.mileage = mileage;
        this.gasTankPercent = gasTankPercent;
    }
}




